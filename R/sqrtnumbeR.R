#' @title Calculate the square of a number
#'
#' @description In this package a function is included to calculate the square of a number.
#'
#' @param number
#'
#' @return square
#'
#' @examples sqrtnumbeR(3)
#'
#' @export

sqrtnumbeR <- function(number) {
  square <- (number*number)
  return(square)
}
